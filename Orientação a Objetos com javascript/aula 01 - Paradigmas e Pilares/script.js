// A programação tem 2 paradigmas o imperativo e o declarativo
/*
-> O imperativo foca em como você vai resolver o problema
-> O declarativo no que e o que você vai fazer

declarativo é um banco de dados por exemplo

na orientação a objetos

-> Os programas são "objetos" que possuem uma série de propriedades.

Pilares:

- Herança
- Polimorfismo
- Encapsulamento
- Abstração


1º Abstração:

"Processo mental que consiste em isolar um aspecto determinado de um estado de coisas relativamente complexo, a fim de simplificar a sua avaliaçao, classificação ou para permitir a comunicação do mesmo."

Resumindo: Você abstrai uma coisa do mundo real para o mundo da programação

2º Herança:

O objeto filho herda propriedades e métodos do objeto pai, as vezes o objeto filho herda as propriedades do pai, porém ele tem outras características também

3º Encapsulamento

Cada classe tem propriedades e métodos independentes do restante do código, Aquela propriedade pertence aquela classe e não altera no restante do código.

4º Polimorfismo

Objetos podem herdar a mesma classe pai, mas se comportarem de forma diferente quando invocamos seus métodos:

Por exemplo, temos um método pai, animal, só que quando invocamos um objeto falar da classe pai, podemos te diversos variações, por exemplo:

Pessoa -> seria falar
Cachorro -> latir
Gato -> Miar
*/